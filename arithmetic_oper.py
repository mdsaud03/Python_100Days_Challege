a=7
b=5

#Addition (+)
print("Addition",a+b)

#Subtraction (-)
print("Subtraction",a-b)

#Multiplication (*)
print("Multiplication",a*b)

#Division (/)
print("Division",a/b)

#Floor Division (//)
print("Floor Division",a//b)

#Modulo (%)
print("Modulo",a%b)

#Exponentiation (**)
print("Exponentiation",a**b)

